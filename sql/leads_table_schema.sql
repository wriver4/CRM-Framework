  name VARCHAR(255),
  phone VARCHAR(20),
  email VARCHAR(255),
  address TEXT,
  estimate_number INT,
  stage VARCHAR(50),
  proposal_sent_date DATE,
  scheduled_date DATE,
  structure_type VARCHAR(50),
  lead_source VARCHAR(50),
  lead_lost_notes TEXT,
  plans_submitted BOOLEAN,
  structure_description TEXT,
  structure_other TEXT,
  site_visit_by VARCHAR(255),
  picture_submitted BOOLEAN,
  referred_to VARCHAR(255),
  picture_upload_link TEXT,
  plans_upload_link TEXT,
  existing_client BOOLEAN,
  get_updates BOOLEAN,
  hear_about VARCHAR(255),
  hear_about_other TEXT,
  structure_additional TEXT,
  lead_notes TEXT,
  prospect_notes TEXT,
  lead_lost BOOLEAN,
  site_visit_completed BOOLEAN,
  closer VARCHAR(255),
  referred_services TEXT,
  assigned_to VARCHAR(255),
  referred BOOLEAN,
  site_visit_date DATE,
  date_qualified DATE,
  contacted_date DATE,
  referral_done BOOLEAN,
  jd_referral_notes TEXT,
  closing_notes TEXT,
  prospect_lost BOOLEAN,
